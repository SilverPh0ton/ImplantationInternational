            <h1>Propositions de séjour</h1>
            <ul>
                <li>Sur cette page, vous pourrez ajouter, voir et modifier les propositions de projet. </li>
                <li>Si la proposition est en bleu, c'est qu'il s'agit d'une nouvelle proposition que vous devez consulter.</li>
                <li><b>À noter: </b> Vous recevrez un courriel pour toutes les propositions de projet qui seront créées.</li>
                <li>Pour accepter une proposition, cliquez sur <img data-toggle="modal" data-target="#myModal_accept" alt="accepter icon" src="Ressource/img/check-solid.png" class="images" data-placement = "top" title = "Accepter"></li>
                <li>Pour refuser une proposition, cliquez sur <img data-toggle="modal" data-target="#myModal_refuse" alt="refuser icon" src="Ressource/img/ban-solid.png" class="images" data-placement = "top" title = "Refuser"></li>
                <li><b>À noter: </b>Lorsque vous accepterez ou refuserez une proposition, un courriel de notification sera envoyé à la personne qui a fait la proposition.</li>
                <br>

                <br>
                <li>
                    Pour voir la liste des propositions, cliquez sur « Propositions de séjour » de la barre de navigation.
                    <ul>
                        <li>Pour afficher plus d’utilisateurs par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                        <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau</li>
                        <li>Pour rechercher une proposition spécifique, entrez soit le nom du projet, le pays, la ville, le coût, ou l’une des dates (limite d’inscription, de départ ou de retour) sous « Recherche » en haut à droite du tableau.</li>
                        <br>
                        <li>
                            Pour faire une proposition de projet, cliquez sur « Ajouter une proposition » au bas de la page
                            <ul>
                                <li>Sur cette page, inscrivez les informations nécessaires, telles que le nom du projet, la destination, la date de départ, celle de retour ainsi que le coût. </li>
                                <li>Dans la section « Ajout des activités », vous pourrez ajouter différentes activités prévues pour ce voyage. Inscrivez l’endroit de l’activité, une courte description, la date de départ et la date de fin. Une fois terminé, cliquez sur <img alt="afficher icon" src="Ressource/img/btPlus.png" class="images" data-toggle="tooltip" data-placement = "top"></li>
                                <ul>
                                    <li>
                                        <b>À noter:</b>La section <b>Endroit</b> de l'activité doit contenir au moins 4 caractères afin que l'activité puisse être ajoutée
                                    </li>
                                </ul>
                                <li>Pour supprimer une activité, cliquez sur <img alt="afficher icon" src="Ressource/img/btPoubelle.png" class="images" data-toggle="tooltip" data-placement = "top"></li>
                                <li>Dans la section « Renseignement supplémentaire », vous trouverez des questions à répondre selon la catégorie. Pour voir les questions, cliquez sur les catégories.</li>
                                <li>Une fois terminé, cliquez sur « Ajouter »</li>
                                <li>Pour revenir à la liste des propositions, cliquez sur « Retour aux propositions »</li>
                            </ul>
                        </li>
                        <li>
                            Pour modifier une proposition de projet, cliquez sur <img alt="afficher icon" src="Ressource/img/eye.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Afficher">
                            <ul>
                                <li>Sur cette page, vous pouvez modifier les informations de cette proposition. </li>
                                <li><b>À noter: </b>Seul un administrateur peut modifier une proposition de séjour en tout temps.</li>

                                <li>Une fois terminé, cliquez sur « Enregistrer »</li>
                                <li>Pour revenir à la liste de proposition, cliquez sur « Retour aux propositions »</li>
                            </ul>
                        </li>
                    </ul>
                </li>
            </ul>
        </div>

        <div id="section1" class="section">
            <h1>Les projets de mobilité</h1>
            <ul>
                <li>
                    Pour voir la liste des voyages, cliquez sur « Séjours de mobilité » de la barre de navigation.
                    <ul>
                        <li>Si le voyage est en gris, c’est qu’il est désactivé.
                            <ul>
                                <li>Pour afficher plus de voyages par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                                <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau</li>
                                <li>Pour rechercher un voyage spécifique, entrez soit le nom du projet, le pays, la ville, le coût, ou l’une des dates (limite d’inscription, de départ ou de retour) sous « Recherche » en haut à droite du tableau</li>
                            </ul>
                    </ul>
                </li>
                <br>
                <li>
                    Pour voir les détails d’un projet, cliquez sur l’onglet <img alt="afficher icon" src="Ressource/img/eye.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Afficher">
                    <ul>
                        <li>Vous pouvez générer un fichier PDF des détails du projet, cliquez sur le bouton  « Générer un PDF »</li>
                        <li>Pour voir les participants du projet, cliquez sur l'onglet <img alt="afficher icon" src="Ressource/img/eye.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Afficher"> de la ligne <b>Participant(s)</b></li>
                        <ul>
                            <li>Une fois sur cette page, vous pouvez aussi générer un fichier PDF incluant les détails de tous les participants de ce projet.</li>
                            <li>Pour revenir à la liste, cliquez sur « Revenir aux détails du projet »</li>
                        </ul>
                        <li>Dans la page de détail, pour voir la proposition d'origine du projet, cliquez sur l’onglet <img alt="afficher icon" src="Ressource/img/eye.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Afficher">de la ligne <b>Proposition d'origine</b></li>
                        <li>Pour revenir à la liste, cliquez sur « Revenir à la liste des voyages »</li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour modifier les détails d’un voyage, cliquez sur l’onglet <img alt="modifier icon" src="Ressource/img/writing.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Modifier">
                    <ul>
                        <li>Dans cet onglet, vous pouvez modifier les informations sur le voyage.</li>
                        <li>Vous pouvez activer ou désactiver un voyage via cette page</li>
                        <li>Une fois terminé, cliquez sur « Enregistrer » au bas de la page </li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour voir les codes d’activation associés à un voyage, cliquez sur l’onglet <img alt="invitation icon" src="Ressource/img/invite.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Code d'activation">
                    <ul>
                        <li>Dans cette page, pour afficher plus de code par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                        <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau</li>
                        <li>Pour rechercher un code spécifique, entrez le code sous « Recherche » en haut à droite du tableau</li>
                        <li>Pour générer des codes d’activation pour le voyage, cliquez sur « Générer des codes » en bas, à gauche du tableau. Dans cette page, entrez le nombre de codes nécessaire et cliquez sur « Générer »</li>
                        <ul>
                            <li><b>À noter:</b> Si un code est en noir, c'est qu'il n'a pas encore été utilisé par un étudiant ou un accompagnateur. Il sera donc inscrit  <b>Oui</b>  dans la colonne « Actif » du tableau. </li>
                            <li><b>À noter:</b> Si un code est en gris, c'est qu'il a été utilisé par un étudiant ou un accompagnateur. Il sera donc inscrit  <b>Non</b>  dans la colonne « Actif » du tableau. </li>
                        </ul>

                        <li>Pour revenir à la liste de codes existants, cliquez sur « Retour à la liste des codes existants »</li>
                        <li>Pour revenir aux voyages, cliquez sur « Revenir à la liste des voyages »</li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour voir les formulaires que les étudiants et enseignants doivent/ont rempli, cliquez sur l’onglet <img alt="formulaire icon" src="Ressource/img/clipboard.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Liste de formulaire">.
                    <ul>
                        <li>Dans cette page, pour afficher plus d’utilisateurs par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                        <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau.</li>
                        <li>Pour rechercher un utilisateur précis, entrez son nom d’utilisateur, son, prénom, son nom ou son type sous « Recherche » en haut à droite du tableau.</li>
                        <li>
                            Sur cette page, vous pouvez aussi voir les formulaires pour chacun des utilisateurs en cliquant sur l’onglet <img alt="afficher icon" src="Ressource/img/eye.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Afficher">
                            <ul>
                                <li>Les formulaires (remplit ou non) sont regroupés par catégorie. Pour les consulter, cliquez sur la catégorie.</li>
                            </ul>
                        </li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour éditer et voir un ou plusieurs formulaires, cliquez sur l’onglet <img alt="formulaire icon" src="Ressource/img/design.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Editeur de formulaire">
                    <ul>
                        <li>
                            Pour ouvrir et fermer une catégorie et voir les questions, cliquez sur le nom de la catégorie
                            <ul>
                                <li>Pour sélectionner ou désélectionner une question du formulaire, cochez ou décochez la case à la gauche du formulaire. </li>
                                <li>Pour sélectionner ou désélectionner toutes les questions dans une même catégorie, cochez ou décochez la case à la gauche de la catégorie.</li>
                                <li>Vous pouvez aussi déplacer une catégorie. Cliquez et gardez enfoncée la touche sur la souris et ensuite déplacez la catégorie de haut en bas. Relâchez ensuite la touche sur la souris pour déposer la question à l’endroit désiré.</li>
                            </ul>
                        </li>
                        <br>
                        <li>
                            Dans cette page, pour modifier une question, cliquez sur l’onglet <img alt="modifier icon" src="Ressource/img/writing.png" class="images" data-toggle="tooltip" data-placement = "top" title = "Modifier">
                            <ul>
                                <li>Pour activer ou désactiver la question, cochez ou décochez la case sous « Actif?»</li>
                                <li>Vous pouvez aussi changer les valeurs reliées à la question.</li>
                                <li>Vous pouvez ajouter plus d’informations dans la section « Informations supplémentaires » au bas de la page.</li>
                            </ul>
                            <br>
                        <li>Une fois terminé, cliquez sur « Enregistrer »</li>
                        <li>Pour revenir à l’éditeur de formulaire, cliquez sur « Retour »</li>
                        </li>

                    </ul>
                </li>
            </ul>
