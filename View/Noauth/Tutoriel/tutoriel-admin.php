                        <li>Pour revenir à la liste, cliquez sur « Revenir à la liste des comptes »</li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour ajouter un utilisateur, cliquez sur le bouton « Ajouter un utilisateur » au page de la page
                    <ul>
