            <div id="section5" class="section">
                <h1>Propositions de séjour</h1>
                <ul>
                    <li>Sur cette page, vous pourrez ajouter, voir et modifier les propositions de voyage.</li>
                    <li>Il est à noter que si la proposition est en <span style="color: red;">rouge</span>, cette proposition à été <span style="font-weight: bold">refusée</span>. S'il est, comme indiqué dans la colonne <span style="font-weight: bold">État</span> du tableau</li>
                    <br>
                    <li>
                        Pour voir la liste de vos propositions, cliquez sur « Propositions de séjour » de la barre de navigation.
                        <ul>
                            <li>Pour afficher plus d’utilisateurs par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                            <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau</li>
                            <li>Pour rechercher une proposition spécifique, entrez soit le nom du projet, le pays, la ville, le coût, ou l’une des dates (limite d’inscription, de départ ou de retour) sous « Recherche » en haut à droite du tableau.</li>
                            <br>
                            <li>
                                Pour faire une proposition de voyage, cliquez sur « Ajouter une proposition » au bas de la page
                                <ul>
                                    <li>Sur cette page, inscrivez les informations nécessaires, telles que le nom du projet, la destination, la date de départ, celle de retour ainsi que le coût. </li>
                                    <li>Dans la section « Ajout des activités », vous pourrez ajouter différentes activités prévues pour ce voyage. Inscrivez l’endroit de l’activité, une courte description, la date de départ et la date de fin. Une fois terminé, cliquez sur <img alt="modifier icon" src="Ressource/img/btPlus.png" class="images" data-toggle="tooltip" data-placement = "top"></li>
                                    <li>Pour supprimer une activité, cliquez sur <img alt="modifier icon" src="Ressource/img/btPoubelle.png" class="images" data-toggle="tooltip" data-placement = "top"></li>
                                    <li>Dans la section « Renseignement supplémentaire », vous trouverez des questions à répondre selon la catégorie. Pour voir les questions, cliquez sur les catégories.</li>
                                    <li>Une fois terminé, cliquez sur « Ajouter »</li>
                                    <li>Pour revenir à la liste des propositions, cliquez sur « Retour aux propositions »</li>
                                </ul>
                            </li>
                            <br>
                            <li>
                                Pour modifier une proposition de voyage, cliquez sur <img alt="modifier icon" src="Ressource/img/writing.png" class="images" data-toggle="tooltip" data-placement = "top">
                                <ul>
                                    <li>Sur cette page, vous pouvez modifier les informations de cette proposition. </li>
                                    <li>Une terminé, cliquez sur « Enregistrer »</li>
                                    <li>Pour revenir à la liste de proposition, cliquez sur « Retour aux propositions »</li>
                                </ul>
                            </li>
                        </ul>
                    </li>
                </ul>
            </div>


        <div id="section1" class="section">
            <h1>Séjours de mobilité</h1>
            <ul>
                <li>
                    Pour voir la liste des voyages, cliquez sur « Séjours de mobilité » de la barre de navigation.
                    <ul>
                        <li>Si le voyage est en gris, c’est qu’il est désactivé.
                            <ul>
                                <li>Pour afficher plus de voyages par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                                <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau</li>
                                <li>Pour rechercher un voyage spécifique, entrez soit le nom du projet, le pays, la ville, le coût, ou l’une des dates (limite d’inscription, de départ ou de retour) sous « Recherche » en haut à droite du tableau</li>
                            </ul>
                    </ul>
                </li>
                <br>
                <li>
                    Pour voir les détails d’un voyage, cliquez sur l’onglet <img alt="modifier icon" src="Ressource/img/eye.png" class="images" data-toggle="tooltip" data-placement = "top">
                    <ul>
                        <li>Pour revenir à la liste, cliquez sur « Revenir à la liste des voyages »</li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour modifier les détails d’un voyage, cliquez sur l’onglet <img alt="modifier icon" src="Ressource/img/writing.png" class="images" data-toggle="tooltip" data-placement = "top">
                    <ul>
                        <li><b>À noter: Si le voyage a déjà été approuvé, vous ne pouvez pas faire de modification</b></li>
                        <li>Dans cet onglet, vous pouvez modifier les informations sur le voyage.</li>
                        <li>Vous pouvez voir la liste des étudiants associée au voyage en cliquant sur « Liste des étudiants »</li>
                        <li>Une fois terminé, cliquez sur « Enregistrer » au bas de la page </li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour voir les codes d’activation associés à un voyage, cliquez sur l’onglet <img alt="modifier icon" src="Ressource/img/invite.png" class="images" data-toggle="tooltip" data-placement = "top">
                    <ul>
                        <li>Dans cette page, pour afficher plus de code par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                        <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau</li>
                        <li>Pour rechercher un code spécifique, entrez le code sous « Recherche » en haut à droite du tableau</li>
                        <li>Pour générer des codes d’activation pour le voyage, cliquez sur « Générer des codes » en bas, à gauche du tableau. Dans cette page, entrez le nombre de codes nécessaire et cliquez sur « Générer »</li>
                        <li>Pour revenir à la liste de codes existants, cliquez sur « Retour à la liste des codes existants »</li>
                        <li>Pour revenir aux voyages, cliquez sur « Revenir à la liste des voyages »</li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour voir les formulaires que les étudiants et vous doivent remplir, cliquez sur l’onglet <img alt="modifier icon" src="Ressource/img/clipboard.png" class="images" data-toggle="tooltip" data-placement = "top">
                    <li><b>À noter: </b> Le seul formulaire que vous pouvez remplir et/ou modifier est le votre. Vous ne pouvez pas modifier les autres formulaires</li>
                    <ul>
                        <li>Dans cette page, pour afficher plus d’utilisateurs par page, choisissez un nombre sous l’onglet « Afficher » en haut à gauche du tableau.</li>
                        <li>Vous pouvez changer de page en cliquant sur « Précédent » et « Suivant » ainsi que les chiffres, en bas à droite du tableau.</li>
                        <li>Pour rechercher un utilisateur précis, entrez son nom d’utilisateur, son, prénom, son nom ou son type sous « Recherche » en haut à droite du tableau.</li>
                        <li>
                            Sur cette page, vous pouvez aussi voir les formulaires pour chacun des utilisateurs en cliquant sur l’onglet <img alt="modifier icon" src="Ressource/img/eye.png" class="images" data-toggle="tooltip" data-placement = "top">
                            <ul>
                                <li>Les formulaires (remplit ou non) sont regroupés par catégorie. Pour les consulter, cliquez sur la catégorie.</li>
                            </ul>
                        </li>
                    </ul>
                </li>
                <br>
                <li>
                    Pour remplir le formulaire qui vous est attitré, cliquez sur « Remplir mon formulaire »
                    <ul>
                        <li>Sur cette page, vous pourrez répondre aux questions dans les différentes catégories.</li>
                        <li>Pour ouvrir et fermer une catégorie, cliquez sur le nom de celle-ci.</li>
                        <li>Une fois terminé, cliquez sur « Enregistrer ».</li>
                        <li>Pour revenir à la liste des formulaires, cliquez sur « Retour aux formulaires »</li>
                    </ul>
                </li>
                <br>
                <li>Pour se joindre à un voyage, entrez votre code d’activation dans la section au bas de la page. Cliquez ensuite sur « Joindre »</li>
            </ul>
        </div>


